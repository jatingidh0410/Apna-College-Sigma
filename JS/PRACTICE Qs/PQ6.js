let size = 'XL';

if (size === 'S'){
    console.log(`Your ${size} size popcorn total is rupees 50.`);
}else if (size === 'M'){
    console.log(`Your ${size} size popcorn total is rupees 100.`);
}else if (size === 'L'){
    console.log(`Your ${size} size popcorn total is rupees 200.`);
}else if (size === 'XL'){
    console.log(`Your ${size} size popcorn total is rupees 250.`);
}else{
    console.log("Particular size not available.");
}